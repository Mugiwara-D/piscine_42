int	ft_atoi(char *str)
{
	unsigned int	i;
	int				n;
	int				rez;

	n = 1;
	i = 0;
	rez = 0;
	while (str[i] >= 9 || str[i] <= 13 || str[i] == 32)
	{
		i++;
	}
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			n *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		rez = (rez * 10) + (str[i] - 48);
		i ++;
	}
	return (rez * n);
}
